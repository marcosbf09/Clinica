    private void SalvarPaciente(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarPaciente
        // TODO add your handling code here:
       EntityManagerFactory emf = Persistence.createEntityManagerFactory("ClinicaPU");
       EntityManager em = emf.createEntityManager();
        
        String nome = jTextField1.getText();
        String idade1 = jTextField2.getText();
        String endereco = jTextField3.getText();
        String rg = jTextField4.getText();
        String cpf1 = jTextField5.getText();
        String data_dia = jTextField6.getText();
        String data_mes = jTextField7.getText();
        String data_ano = jTextField8.getText();
        String telefone = jTextField9.getText();
        String celular = jTextField10.getText();
        String email = jTextField11.getText();
        int conv = 1;
        
        if(jRadioButton1.isSelected()){
             conv=1;
        }
        if(jRadioButton2.isSelected()){
             conv=2;
        }
        
        int idade, cpf, dia, mes, ano;
        
        idade = Integer.parseInt(idade1);
        cpf = Integer.parseInt(cpf1);
        dia = Integer.parseInt(data_dia);
        mes = Integer.parseInt(data_mes);
        ano = Integer.parseInt(data_ano);
        
        
        em.getTransaction().begin();


        gerenciaPacientes.cadastrar(em, nome, idade, endereco, rg, cpf, dia, mes, ano, telefone, celular, email, conv);

        em.getTransaction().commit();

        StringBuffer mensagem = new StringBuffer();

        mensagem.append("\nOs Dados do paciente " + nome + " foram salvos com sucesso!");

        JOptionPane.showMessageDialog(rootPane, mensagem);
        
        new TelaGerenciarPacientes().setVisible(rootPaneCheckingEnabled);       
        dispose();    
        
        
    }//GEN-LAST:event_SalvarPaciente

