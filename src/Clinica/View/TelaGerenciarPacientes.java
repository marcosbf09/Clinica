    private void Remover(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Remover
        // TODO add your handling code here:
        
        String cpfs = jTextField1.getText(); //recebendo a entrada de dados
        int cpf1 = Integer.parseInt(cpfs); //passando cpf recebido na tela para tipo int para buscar o paciente no banco
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ClinicaPU");
        EntityManager em = emf.createEntityManager();
        
        em.getTransaction().begin();
        
        
        Cliente c = em.find(Cliente.class, cpf1); //procurando o cliente no banco
            
        em.remove(c); //removendo o cliente
        em.getTransaction().commit();
        
        StringBuffer mensagem = new StringBuffer();

        mensagem.append("PACIENTE REMOVIDO!");

        JOptionPane.showMessageDialog(rootPane, mensagem);
        
    }//GEN-LAST:event_Remover

