    private void ConsultarDados(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarDados
        // TODO add your handling code here:
        String cpf1 = jTextField1.getText();
        int cpfconsulta = Integer.parseInt(cpf1);
        
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ClinicaPU");
        EntityManager em = emf.createEntityManager();
        
        em.getTransaction().begin();
            
            Cliente c = em.find(Cliente.class, cpfconsulta);
           
            String nome = c.getNome();
            String rg = c.getRg();
            int cpf = c.getCpf();
            int idade = c.getIdade();
            String datanasc = c.getDataNascimento();
            String endereco = c.getEndereço();
            String telefone = c.getTelefone();
            String celular = c.getCelular();
            String email = c.getEmail();
            String plano = c.getPlano();
            
            StringBuffer mensagem = new StringBuffer();

            mensagem.append("DADOS DO PACIENTE: \n\nNome: "+ nome + "\nRG: " + rg + "\nCPF: " + cpf + "\nIdade: " + idade 
                    + "\nData de Nascimento: " + datanasc + "\nEndereco: " + endereco + "\nTelefone: " + telefone + 
                    "\nCelular: " + celular + "\nEmail: " + email + "\nConvênio: " + plano);

            JOptionPane.showMessageDialog(rootPane, mensagem);
            
            
            em.getTransaction().commit();

    }//GEN-LAST:event_ConsultarDados

